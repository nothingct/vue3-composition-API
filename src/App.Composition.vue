<template>
  <h1 @click="increase">
    {{ count }} / {{ doubleCount }}
  </h1>
  <h1>
    {{ message }} / {{ reversedMessage }}
  </h1>
</template>

<script>
import { ref, computed } from 'vue'
export default {
  setup(){
    const message = ref('Here Comes The Life')
    const reversedMessage = computed(()=>message.value.split('').reverse().join(''))
    
    const count =ref(0)
    const doubleCount = computed(()=>count.value*2)
    function increase(){
      count.value+=1
    }

    return {
      message,
      reversedMessage,
      count,
      doubleCount,
      increase

    }
  }  
}
</script>